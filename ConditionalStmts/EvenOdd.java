// write a program to check whethr the given number is odd or even.
class EvenOdd	 {
	public static void main(String[] args) {
		
		int num=33;

		
		if(num%2==0){
			System.out.println(num + " number is even ");
		}
		
		else{
			System.out.println(num + " number is odd ");
		}	
		
	}
}